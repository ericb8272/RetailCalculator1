    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double sale = 0, price, percent;
        jTable1.setValueAt(jTextField1.getText(), 0, 0);
        jTable1.setValueAt(department, 0, 1);
        jTable1.setValueAt(jTextField2.getText(), 0, 2);
        try {
            price = Double.valueOf(jTextField2.getText());
            percent = Double.valueOf(jTextField3.getText());
            sale = (price) * (percent / 100) - price;
        } catch (Exception ae) {
        }
        jTable1.setValueAt("$" + sale, 0, 3);
        jTable2.setValueAt(jTextField1.getText(), num, 0);
        jTable2.setValueAt(department, num, 1);
        jTable2.setValueAt("$" + jTextField2.getText(), num, 2);
        jTable2.setValueAt("$" + sale, num, 3);
        num++;
    }//GEN-LAST:event_jButton1ActionPerformed

